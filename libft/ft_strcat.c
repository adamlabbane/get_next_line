char	*ft_strcat(char *dest, const char *src)
{
	int i;
	int k;

	k = 0;
	i = 0;
	while (dest[k])
		k++;
	while (src[i] != '\0')
	{
		dest[k] = src[i];
		i++;
		k++;
	}
	dest[k] = '\0';
	return (dest);
}
